/* 억원 → "n조 n,nnn억" 표기 */
function fmtEok(v){
  v = Math.round(v*100)/100;
  if (v >= 10000){
    const jo = Math.floor(v/10000);
    const rest = Math.round(v - jo*10000);
    return rest>0 ? `${jo}조 ${rest.toLocaleString()}억` : `${jo}조`;
  }
  if (Number.isInteger(v)) return `${v.toLocaleString()}억`;
  return `${v.toLocaleString()}억`;
}

